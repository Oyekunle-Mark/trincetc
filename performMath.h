#ifndef TRINCETC_PERFORMMATH_H
#define TRINCETC_PERFORMMATH_H

double add(double x, double y) {
    return x + y;
}

double sub(double x, double y) {
    return x - y;
}

double mult(double x, double y) {
    return x * y;
}

double div(double x, double y) {
    return x / y;
}

#endif //TRINCETC_PERFORMMATH_H
